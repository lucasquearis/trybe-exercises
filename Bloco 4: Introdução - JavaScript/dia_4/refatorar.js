let valorA = 5;
let valorB = 5;

console.log('valor A é = ' + valorA + ' valor B é = ' + valorB)

//Script de soma//
function operacaoSoma(){
  let resultado = valorA + valorB;
  console.log('Resultado soma ' + resultado);
}

operacaoSoma(valorA, valorB);

//Script de Subtração

function operacaoSubtracao(){
  let resultado= valorA - valorB;
  console.log('Resultado Subtração ' + resultado);
}

operacaoSubtracao(valorA, valorB)

//Script de Multiplicação

function operacaoMultiplicacao(){
  let resultado = valorA * valorB;
  console.log('Resultado Multiplicação ' + resultado);
}
operacaoMultiplicacao(valorA, valorB)

//Script de Divisão

function operacaoDivisao(){
  let resultado = valorA / valorB;
  console.log('Resultado Divisão ' + resultado);
}
operacaoDivisao(valorA, valorB);

//Script de Módulo

function operacaoModulo(){
  let resultado = valorA % valorB;
  console.log('Resultado Módulo ' + resultado);
}
operacaoModulo(valorA, valorB);